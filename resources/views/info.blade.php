					<p>Las fobias también se engloban dentro de los trastornos de ansiedad. Las fobias pueden ser muy dispares, desde hablar en público hasta el pánico a las serpientes. Cuando estas fobias interrumpen sus actividades diarias empiezan a ser un problema.</p>

					<p>Un ejemplo sería la fobia social. Usualmente comienza en la adolescencia. Se convierte en un problema o trastorno cuando pasan días y semanas preocupándose por las relaciones sociales. Al igual que con el trastorno de pánico, la ansiedad social puede conducir a la agorafobia.</p>

					<p>La medicación y la terapia puede ayudar a aliviar la ansiedad y permitir que los pacientes puedan funcionar normalmente.</p>
