					<h4>Trastornos de la Personalidad</h4>
					<p>Los seres humanos tienen una colección de rasgos de la personalidad que se basan en nuestras experiencias e influyen en nuestra interacción social, nuestro comportamiento y forma de pensar. Estos rasgos están profundamente arraigados en nuestra cultura. Las personas con trastornos de la personalidad tienen rasgos de carácter muy rígidos que pueden afectar a su capacidad para funcionar en situaciones cotidianas.</p>

					<p>Entre los trastornos de personalidad más comunes se encuentran:</p>

					<p><b>* Trastorno de la personalidad antisocial:</b> se refiere a aquellas personas que no siguen las reglas sociales y tienen poca consideración por los sentimientos de los demás. A menudo muestran un comportamiento criminal y no muestran remordimiento.</p>

					<p><b>* Trastorno límite de personalidad:</b> este trastorno hace que la gente sea inestable e impulsiva. Temen el abandono y tienen dificultades para mantener relaciones estables.</p>

					<p>El diagnóstico y tratamiento de los trastornos de la personalidad son difíciles porque hay muchos factores biológicos y ambientales que interfieren. Además, muchas personas que sufren algún tipo de trastorno de la personalidad suelen padecer algún otro tipo de trastorno mental, como los trastornos de ansiedad, el TDAH, la depresión y el trastorno bipolar.</p>
