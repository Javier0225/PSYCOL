					<h4>Trastorno Bipolar</h4>
					<p>El trastorno bipolar, englobado en los llamados trastornos del estado de ánimo, provoca en las personas que lo padecen emociones muy exageradas (son los llamados episodios maníacos) para después pasar a niveles anímicos peligrosamente bajos (depresión). Los extremos son tan drásticos que pueden dañar las relaciones sociales, provocando como resultado un bajo rendimiento en la escuela o el trabajo y con riesgo de suicidio.</p>

					<p>La mayoría de las personas con trastorno bipolar son diagnosticados a los 25 años, pero muchas veces esta enfermedad no es fácil de detectar. Dado que las pruebas de sangre y escáneres cerebrales no pueden detectar el trastorno bipolar, los profesionales de la salud mental se basan en la historia clínica del paciente. Las personas con trastorno bipolar pueden llevar una vida normal con un tratamiento adecuado que combine medicación y psicoterapia para controlar los síntomas y reducir el riesgo de futuros episodios.</p>
