					<h4>Déficit de Atención e Hiperactividad</h4>
					<p>¿Te acuerdas del compañero de tu colegio que no podía quedarse quieto, siempre estaba hablando o interrumpiendo, se le olvidaba hacer la tarea y parecía perderlo todo? Lo más probable es que el niño sufría de trastorno de hiperactividad y déficit de atención (TDAH).</p>

					<p>El <b>TDAH</b> es un trastorno común en la niñez. Por lo general se diagnostica en niños muy activos que tienen dificultades para concentrarse y controlar su comportamiento. La genética pone algunos niños en situación de riesgo.

					<p>Para diagnosticar y posteriormente tratar el TDAH, los profesionales médicos evalúan primero la salud física del niño para descartar otras enfermedades, como la mala visión o pérdida de audición. Las observaciones y los informes de los maestros y los miembros de la familia ayudan a un médico a establecer un diagnóstico y comenzar el tratamiento. Muchos niños reciben medicación y terapia conductual.</p>

					<p>La mayoría de los niños con TDAH superan o aprenden a manejar muchos de los síntomas. Sin embargo, algunos continúan teniendo este trastorno en su vida adulta, por lo que su vida personal y profesional resulta difícil de manejar.</p>
