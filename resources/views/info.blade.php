					<h4>Trastornos de la alimentación</h4>
					<p>Cuando una persona con anorexia se ve en el espejo, él o ella ve un reflejo de sobrepeso, no el cuerpo desnutrido que en realidad se refleja. La falta de alimentación y la obsesión con la comida puede llevar a la gente con anorexia a numerosos problemas de salud potencialmente mortales, como la desnutrición.</p>

					<p>La bulimia nerviosa se produce en aquellas personas que consumen grandes cantidades de comida y luego, lamentando su comportamiento y la falta de control, se purgan a base de vómitos, uso de laxantes, ayuno o ejercicio obsesivo.</p>

					<p>Los trastornos alimenticios se pueden tratar con tratamientos personalizados que ofrecen medicación, consejería nutricional y terapia individual y de grupo.</p>
