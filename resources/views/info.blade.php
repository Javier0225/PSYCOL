					<h4>Trastornos de Ansiedad</h4>
					<p>Los trastornos de ansiedad engloban una serie de enfermedades mentales:</p>

					<p>-El trastorno obsesivo-compulsivo (TOC) es una combinación de pensamientos recurrentes (obsesiones) y acciones repetitivas (compulsiones) que realiza una persona con la creencia de que el comportamiento le da el control de la obsesión. Por ejemplo, una mujer obsesionada con la limpieza puede sentir la necesidad de lavarse las manos una y otra vez. Algunas personas con TOC desarrollan rituales diarios muy complejos.</p>

					<p>-Trastorno de estrés postraumático (TEPT) se produce después de una experiencia aterradora en la que la persona sintió la amenaza de daño físico o miedo, horror o impotencia.</p>

					<p>-Trastorno de ansiedad generalizada (TAG) Las personas con TAG se preocupan demasiado por las preocupaciones cotidianas y, a menudo esperan desastres. Cuando su nivel de ansiedad se eleva, son un “manojo de nervios” y experimentan síntomas físicos tales como tensión muscular, sudoración, temblores y náuseas.</p>

					<p>Muchas personas que sufren de trastornos de ansiedad se automedican con alcohol y drogas. Sin embargo, se debe buscar ayuda profesional porque, como el trastorno de pánico, los trastornos de ansiedad se pueden tratar con medicamentos y terapia.</p>
