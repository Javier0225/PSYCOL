					<h4>Trastornos del estado de ánimo</h4>
					<p>Los estados de ánimo son emociones sostenidas, que colorean la manera de ver la vida. Cuando alguien tiene un trastorno del estado de ánimo, el panorama general no es color de rosa. Las personas con estos trastornos tienen estados de ánimo que van más allá de sentirse “tristes”. Los trastornos del estado de ánimo se pueden encontrar en todas las razas y clases sociales, pero son más comunes entre las personas solteras.</p>

					<p>Son los trastornos mentales más frecuentemente diagnosticados. Además del trastorno bipolar, los trastornos del estado de ánimo incluyen el trastorno depresivo mayor y el trastorno distímico.</p>

					<p>La depresión mayor se caracteriza por una tristeza profunda persistente. Los síntomas como la fatiga, falta de concentración, cambios en el apetito y pensamientos de suicidio interfieren con el funcionamiento diario. Las personas con distimia manifiestan síntomas menos severos pero más duraderos. Otras formas de depresión son la depresión posparto.</p>

					<p>La depresión es un trastorno cerebral causado muy probablemente por una combinación de factores genéticos, ambientales, biológicos y psicológicos. Afortunadamente, la depresión puede ser tratada y los mejores resultados se logran cuando el tratamiento se inicia tan pronto como se diagnostica la depresión. La mayoría de las personas con trastornos del estado de ánimo se benefician de los medicamentos antidepresivos y la psicoterapia. La clave para el éxito del tratamiento de los trastornos del estado de ánimo y la prevención de futuros episodios es continuar tomando los medicamentos y la búsqueda de ayuda.</p>
